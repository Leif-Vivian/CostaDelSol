function precast(spell)
	if spell.type:contains('Magic') then
		equip(sets.precast.fc)
	end
end
	
function midcast(spell)
	if spell.skill == 'Elemental Magic' then
		equip(sets.midcast.nuke)
	if player.target.name == 'Tiamat' then
		equip(sets.midcast.skill)
	elseif player.target.name == 'Cerberus' then
		equip(sets.midcast.skill)
	elseif player.target.name == 'Jormungand' then
		equip(sets.midcast.skill)
	elseif player.target.name == 'Khimaira' then
		equip(sets.midcast.skill)
	elseif player.target.name == 'Sarameya' then
		equip(sets.midcast.skill)
	end
	
		if world.day == 'Firesday' then
			equip(sets.special.sandals)
		end	
		
		if (world.day == 'Iceday' or buffactive['Hailstorm'] or world.weather == 'Snow' or world.weather == 'Blizzards') and spell.english:startswith('Blizzard') then
			equip(sets.special.iceday)
		elseif (world.day == 'Firesday' or buffactive['Firestorm'] or world.weather == 'Hot Spells' or world.weather == 'Heat Waves') and spell.english:startswith('Fire') then
			equip(sets.special.firesday)
		elseif (world.day == 'Lightningsday' or buffactive['Thunderstorm'] or world.weather == 'Thunder' or world.weather == 'Thunderstorms') and spell.english:startswith('Thunder') then
			equip(sets.special.lightningday)
		elseif (world.day == 'Windsday' or buffactive['Windstorm'] or world.weather == 'Winds' or world.weather == 'Gales') and spell.english:startswith('Aero') then
			equip(sets.special.windsday)
		elseif (world.day == 'Earthsday' or buffactive['Sandstorm'] or world.weather == 'Dust storms' or world.weather == 'Sand storms') and spell.english:startswith('Stone') then
			equip(sets.special.earthsday)
		elseif (world.day == 'Watersday' or buffactive['Rainstorm'] or world.weather == 'Rain' or world.weather == 'Squalls') and spell.english:startswith('Water') then
			equip(sets.special.watersday)
		end
		
		if spell.english:endswith('helix') then
			equip(sets.midcast.helix)
		end
	end
		
	if spell.skill == 'Healing Magic' then
		equip(sets.midcast.cure)
		if spell.english:endswith('na') then
			equip(sets.midcast.haste)
		elseif spell.english:startswith('Raise') then
	        equip(sets.midcast.haste)	
		elseif spell.english:startswith('Reraise') then
	        equip(sets.midcast.haste)
		elseif (world.day == 'Lightsday' or buffactive['Aurastorm'] or world.weather == 'Auroras' or world.weather == 'Stellar glare') and spell.english:startswith('Cur') then
			equip(sets.special.lightsday)			
		end
	end
	
	if spell.skill == 'Enhancing Magic' then
		equip(sets.midcast.haste)
		if spell.english:startswith('Phalanx') then
	        equip(sets.midcast.enhancing)
        elseif spell.english:startswith('Bar') then
	        equip(sets.midcast.enhancing)
		elseif spell.english:startswith('En') then
	        equip(sets.midcast.enhancing)
		elseif spell.english:startswith('Stoneskin') then
		    equip(sets.midcast.stoneskin)
		end	
	end
	
	if spell.skill == 'Dark Magic' then
		equip(sets.midcast.dark)
		if world.day == 'Firesday' then
			equip(sets.special.sandals)	
		elseif (world.day == 'Darksday' or buffactive['Voidstorm'] or world.weather == 'Gloom' or world.weather == 'Darkness') and spell.english:startswith('Drain') then
			equip(sets.special.darksday)
		elseif (world.day == 'Darksday' or buffactive['Voidstorm'] or world.weather == 'Gloom' or world.weather == 'Darkness') and spell.english:startswith('Aspir') then
			equip(sets.special.darksday)
		
		end
	end	
		
		if spell.name == "Kaustra" then
			equip(sets.midcast.kaustra)
		if (world.day == 'Darksday' or buffactive['Voidstorm'] or world.weather == 'Gloom' or world.weather == 'Darkness') and spell.english:startswith('Kaustra') then
			equip(sets.special.darksday)
		end
	end
		
		if spell.name == 'Klimaform' then
			equip(sets.midcast.haste)
		end

	if spell.skill == 'Enfeebling Magic' then
		equip(sets.midcast.enfeeb)
		if spell.english:startswith('Gravity') then
			equip(sets.special.INTfeeble)
		elseif spell.english:startswith('Blind') then
			equip(sets.special.INTfeeble)
		elseif spell.english:startswith('Bind') then
			equip(sets.special.INTfeeble)
		elseif spell.english:startswith('Sleep') then
			equip(sets.special.INTfeeble)
		elseif spell.english:startswith('Poison') then
			equip(sets.special.INTfeeble)
		elseif spell.english:startswith('Dispel') then
			equip(sets.special.INTfeeble)
		end
	end
end
	
function aftercast(spell,action)
	if player.status == 'Idle' and buffactive['Sublimation: Activated'] then
		equip(sets.idle.subcharging)
	elseif player.status == 'Idle' and buffactive['Sublimation: Complete'] then
		equip(sets.idle.sublimation)
	elseif player.status == 'Idle' then
		equip(sets.idle.sublimation)
	elseif player.status == 'Engaged' then
		equip(sets.idle.sublimation)
	end
end
	
function status_change(new,old)
	if new == 'Resting' then
		equip(sets.idle.rest)
	elseif new == 'Idle' and buffactive['Sublimation: Activated'] then
		equip(sets.idle.subcharging)
	elseif new == 'Idle' then
		equip(sets.idle.sublimation)
	elseif new == 'Engaged' then
		equip(sets.idle.sublimation)
	end
end
 
function buff_change(name,gain)
    if name == 'Sublimation: Complete' and gain == 'True' then
		equip(sets.idle.sublimation)
	end
end